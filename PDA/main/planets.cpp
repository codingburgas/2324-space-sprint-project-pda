#include"planets.h"

void mercury() {

    const int screenWidth = GetScreenWidth();
    const int screenHeight = GetScreenHeight();

    while (!WindowShouldClose()) {
        BeginDrawing();
        DrawRectangle(0, 0, screenWidth, screenHeight, WHITE);
        DrawText("tova e mercury", 50, 50, 100, GREEN);
        EndDrawing();
    }
}
